    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        int prvi = Integer.parseInt(jLabel1.getText());
        int drugi = Integer.parseInt(jLabel2.getText());
        int treci = Integer.parseInt(jLabel3.getText());
        int cetvrti = Integer.parseInt(jLabel4.getText());
        
        CommonSkocko skocko = new CommonSkocko(prvi,drugi,treci,cetvrti);
        try {
            skocko = Controller.getInstance().posalji(skocko);
            igra.add(skocko);
            updateTable();
        } catch (Exception e) {
        }
        
        if(skocko.pobedioJe()){
            JOptionPane.showMessageDialog(this, "Kombinacija je tacna, pobedili ste !!!");
        
        }
        if(skocko.isPoslednja()){
            JOptionPane.showMessageDialog(this, "Niste pogodili tacnu kombinaciju za 6 rundi, izgubili ste !");
            
        }
    }//GEN-LAST:event_btnSendActionPerformed

