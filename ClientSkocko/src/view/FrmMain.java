    private void updateTable() {
        
        dtm.setRowCount(0);
        int i = 0;
        for(CommonSkocko skocko : igra){
            dtm.addRow(new Object[]{String.valueOf(i++), skocko.getPogodjeniNaMestu(), skocko.getPogodjeniNisuNaMestu() });
        
        }
        
        dtm.fireTableDataChanged();
       
    }
